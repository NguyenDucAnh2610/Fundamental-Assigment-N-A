const promNumber = prompt('Please input a number from 1 to 7');
var number = parseFloat(promNumber);

if (number === 1) {
    alert('Sunday');
} else if (number === 2) {
    alert('Monday');

}  else if (number === 3) {
    alert('Tuesday');

}  else if (number === 4) {
    alert('Wednesday');

}  else if (number === 5) {
    alert('Thursday');

}  else if (number === 6) {
    alert('Friday');

}  else if (number === 7) {
    alert('Saturday');

} else {
    alert('please input 1 to 7');

} 